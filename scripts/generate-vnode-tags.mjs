#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const vnodePath = path.join(repoRoot, "vnode.mbt");

const markerStart = "// BEGIN GENERATED TAG HELPERS";
const markerEnd = "// END GENERATED TAG HELPERS";

const elementTags = [
  "div",
  "span",
  "button",
  "h1",
  "h2",
  "h3",
  "p",
  "ul",
  "li",
  "label",
  "section",
  "header",
  "footer",
  "a",
  "form",
  "textarea",
  "select",
  "option",
  "table",
  "tr",
  "td",
  "th",
  "nav",
  "pre",
  "code",
  "em",
  "strong",
];

const voidTags = [
  ["input_", "input"],
  ["img", "img"],
  ["br", "br"],
  ["hr", "hr"],
];

function elementHelper(name, tag) {
  return `///|
pub fn[Msg] ${name}(
  attrs : Array[Attr[Msg]],
  children : Array[VNode[Msg]],
) -> VNode[Msg] {
  el("${tag}", attrs, children)
}
`;
}

function voidHelper(name, tag) {
  return `///|
pub fn[Msg] ${name}(attrs : Array[Attr[Msg]]) -> VNode[Msg] {
  el("${tag}", attrs, [])
}
`;
}

const generated = [
  markerStart,
  "// Generated by scripts/generate-vnode-tags.mjs. Do not edit manually.",
  ...elementTags.map((tag) => elementHelper(tag, tag).trimEnd()),
  ...voidTags.map(([name, tag]) => voidHelper(name, tag).trimEnd()),
  markerEnd,
].join("\n\n");

const source = fs.readFileSync(vnodePath, "utf8");
const pattern = new RegExp(
  `${markerStart}[\\s\\S]*${markerEnd}`,
  "m",
);

if (!pattern.test(source)) {
  console.error(
    `Unable to locate generation markers in ${vnodePath}: ${markerStart} ... ${markerEnd}`,
  );
  process.exit(1);
}

const next = source.replace(pattern, generated);
fs.writeFileSync(vnodePath, next);
console.log(`Updated ${path.relative(repoRoot, vnodePath)}`);
